from __future__ import annotations

from typing import Any, Dict, List


def build_stage1_messages(target_image_data_url: str) -> List[Dict[str, Any]]:
    system = (
        "你是一名严格的医学图像分诊助手。"
        "你的任务是判断给定图片是否属于由多个视觉子图/面板组成的复合图。"
        "这里的 composite 指：一张最终图片中至少包含两个彼此独立、可以分别描述的子图或面板。"
        "例如：网格拼图、左右对比图、示意图和内镜图并列、带 A/B/C 或 1/2/3 标记且语义上可分开的多面板图。"
        "箭头、局部插图标记、单一场景上的注释，不应判为 composite。"
        "如果若干画面虽然看起来是多帧，但它们之间具有强顺序依赖，必须合在一起按步骤阅读才能表达一个完整的手术或操作过程，"
        "例如分步骤手术 montage，这类图不应判为 composite，而应视为一张整体图。"
        "只返回 JSON，且只包含以下键：is_composite, estimated_subfigure_count, confidence, reason。"
    )
    user_content = [
        {"type": "text", "text": "请判断这张图片是否属于可拆分的复合多面板图。"},
        {"type": "image_url", "image_url": {"url": target_image_data_url}},
    ]
    return [
        {"role": "system", "content": system},
        {"role": "user", "content": user_content},
    ]


def build_stage2_messages(
    *,
    target_image_data_url: str,
    source_final_description: str,
) -> List[Dict[str, Any]]:
    system = (
        "你是一名精确的医学复合图拆分助手。"
        "你会收到："
        "1）一张待判断/待拆分的复合图候选图片；"
        "2）该整张图对应的完整整图描述。"
        "你的任务是："
        "A）先判断这张图是否真的应该被拆成多个相互独立的子图；"
        "B）如果应该拆分，识别图中所有有意义的子图/面板；"
        "C）对每个子图，返回其在整张图上的归一化整数边界框 [x0,y0,x1,y1]，坐标范围为 0-1000；"
        "D）基于整图描述，为每个子图写出对应的子图描述。"
        "规则如下："
        "1）如果图中包含多个在视觉上彼此独立、可以分别框出的 panel，即使这些 panel 之间存在步骤顺序、箭头连接或流程关系，也仍然应该拆分；只要每个 panel 本身是独立可见的图像面板，就把它视为可拆分的子图。"
        "2）只有当整张图本质上是单一连续画面，或多个局部内容无法在视觉上分成独立 panel、必须作为同一整体一起理解时，才不要拆分；如果按照本规则不应拆分，则返回空的 subfigures 列表。"
        "3）纯文字 caption、装饰性留白、箭头、覆盖在图上的标签，不要当作独立子图。"
        "4）先做视觉定位，再做语义匹配。必须先根据图中真实可见的 panel 边界确定每个 bbox，再结合整图描述填写 description。不要先根据整图描述里的步骤数、编号或叙述顺序，直接脑补一个规则网格。"
        "5）每个 bbox 必须覆盖完整 panel 的主要图像区域，不能只框到某个 panel 的左半边、右半边、上半边、中间一块，不能截断 panel。"
        "6）如果图中存在编号、箭头、连接线、面板下方或旁边的说明文字，这些内容只用于辅助识别对应关系，不应用它们本身充当 panel 边界。panel 边界应以真实图像内容的外接矩形为准。"
        "7）优先保证语义上正确的子图划分，同时边界框要尽量贴紧该子图的真实视觉内容。除非只保留极小安全边距，否则不要把大块外部白边、宽的空白间隙、箭头区域或附近空白区域框进去。"
        "8）所有返回的子图框都必须完全落在整张图内部。以整张图左上角作为坐标原点 (0,0)，x 轴从左向右增大，y 轴从上向下增大。边界框格式固定为 [x0, y0, x1, y1]，并满足 x0 < x1、y0 < y1。"
        "9）允许不同 panel 的宽高不一致。如果图中 panel 不是严格规则网格，不要输出等宽等高的模板化切块。只有在视觉上确实是规则网格时，才可以返回规则网格对应的框。"
        "10）明确禁止把整张图的主体内容区域按子图数量做矩形均分覆盖。例如，不要为了得到 6 个子图，就把整块区域简单切成 3x2 的等分矩形；如果多个 bbox 几乎等宽等高、并且能够无缝拼成一个完整大矩形，但这些边界并不对应真实可见的 panel 边界，则这种答案是错误的，必须重新依据真实 panel 边界定位。"
        "11）每个子图描述都要以整图描述作为主要语义来源。如果整图描述没有逐一明确列出每个子图，也要结合整段描述推断该子图最可能对应的描述，而不是退化成泛泛的纯视觉短语。"
        "12）不要把同一段整图描述原样复制给每个子图。每个子图描述都要聚焦该子图本身，同时与整图语义保持一致。"
        "13）每个子图描述都应是完整、信息充分的一到两句话，而不是一个简短短语。除了子图特有信息，还应在必要时保留共通背景，例如解剖部位、疾病背景、成像方式或操作语境，使该描述单独拿出来也自洽、有信息量。"
        "14）输出前必须自检：确认每个框都覆盖完整 panel，没有明显 panel 被截断、漏掉，也没有因为说明文字、箭头或留白而把框切错。"
        "只返回 JSON，且只包含键：subfigures。"
        "每个 subfigure 只包含：bbox_norm1000_xyxy, description。"
    )
    user_content = [
        {"type": "text", "text": f"这张整图的完整描述如下：{source_final_description}"},
        {"type": "text", "text": "下面是待判断/待拆分的图片："},
        {"type": "image_url", "image_url": {"url": target_image_data_url}},
    ]
    return [
        {"role": "system", "content": system},
        {"role": "user", "content": user_content},
    ]
